package world;

public class ItemData {

    private String id;
    private String type;
    private String name;
    private String description;
    private boolean portable;

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public boolean isPortable() {
        return portable;
    }
}
